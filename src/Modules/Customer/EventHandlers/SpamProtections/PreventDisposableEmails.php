    /**
     * @param array $data
     *
     * @return bool
     * @throws \Throwable
     */
    public function isSpam (array $data): bool
    {
        $content = $data[CustomerSettingsController::SpamProtection_PreventDisposableEmailsInput] ?? null;
        $moreDomains = $data[CustomerSettingsController::SpamProtection_PreventDisposableEmailsMoreDisposableDomains] ?? '';
        $moreDomains = explode(',', $moreDomains);
        if (!empty($moreDomains)) {
            $moreDomains = array_flip($moreDomains);
        }

        if ($content === null) {
            return false;
        }

        $inputs = explode(',', $content);
        $isSpam = false;
        foreach ($inputs as $input) {

            if (input()->fromPost()->has($input) === false) {
                $isSpam = true;
                break;
            }

            if (input()->fromPost()->hasValue($input)) {
                # Assuming the email has been validated which has been done with the rule checker
                $emailValue = input()->fromPost()->retrieve($input);
                $domain = mb_strtolower(explode('@', trim($emailValue))[1]);
                if (isset($this->disposableEmails()[$domain]) || isset($moreDomains[$domain])) {
                    $isSpam = true;
                    break;
                }
            }
        }

        return $isSpam;
    }

